package br.com.socialfolio.socialfolioapi.user;

/*
   Em Java, um enum (abreviação de "enumeration" - enumeração)
   é um tipo de dado especial que permite definir um conjunto fixo de
   constantes nomeadas dentro de uma única estrutura.
   Em essência, um enum é usado para representar um grupo de
   valores pré-definidos que são mutuamente exclusivos.
*/
public enum Role {
    USER,
    ADMIN
}
